package HW2;

/**
 * Represents the winner of a Connect Four Game.
 */
public enum GameWinner {
        /**
         * Indicates the Connect Four Game ended in a draw.
         */
        DRAW,
        /**
         * Indicates the Connect Four Game is still in progress.
         */
        NONE,
        /**
         * Indicates Player A won the Connect Four Game.
         */
        A,
        /**
         * Indicates Player B won the Connect Four Game.
         */
        B
}
