package HW2;

/**
 * Represents the state of a Connect Four Game. The game is either
 * {@code OVER} or {@code IN_PROGRESS}.
 */
public enum GameState {
   /**
    * Indicates the Connect Four Game is over.
    */
   OVER,
   /**
    * Indicates the Connect Four Game is still in progress.
    */
   IN_PROGRESS, 
}
