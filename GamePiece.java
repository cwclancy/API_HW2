package HW2;
/**
 * Represents a piece in a Connect Four Game. Player A places
 * A, Player B places B, and NONE indicates a Board Square without a piece.
 */
public enum GamePiece {
    /**
     * Indicates player A placed the piece.
     */
    A,
    /**
     * Indicates the Board Square with no piece.
     */
    NONE,
    /**
     * Indicates player B placed the piece.
     */
    B
}





